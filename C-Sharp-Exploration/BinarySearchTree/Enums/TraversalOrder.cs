namespace BinarySearchTree.Enums
{
    public enum TraversalOrder
    {
        /// <summary>
        /// Traverses from left, to mid, to right
        /// </summary>
        InOrder,

        /// <summary>
        /// Traverses from left, to right, to mid
        /// </summary>
        PostOrder,

        /// <summary>
        /// Traverses from mid, to left, to right
        /// </summary>
        PreOrder
    }
}
